function I = int_trapezoidal_rule(fun,a,b,n)
%-------------------------------------------------------------%
%   Program written using GNU Octave, compatable with MATLAB  %
%   James Vrionis                                             %
%   int_trapezoidal_rule.m                                    %
%                                                             %
%-------------------------------------------------------------%

%-------------------------------------------------------------%
h = (b-a)/n;
result = 0.5*fun(a) + 0.5*fun(b);
for i = 1:(n-1)
    result = result + fun(a + i*h);
end
%-------------------------------------------------------------%

%-------------------------------------------------------------%
I = h*result;
end
%-------------------------------------------------------------%
